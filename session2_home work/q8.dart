void main() {
  double num1 = 10;
  double num2 = 3;

  double addition = num1 + num2;
  double subtraction = num1 - num2;
  double multiplication = num1 * num2;
  double division = num1 / num2;
  double modulo = num1 % num2;

  print("The sum of the two numbers is $addition");
  print("The result of subtracting the two numbers is $subtraction");
  print("The product of the two numbers is $multiplication");
  print("The quotient of the two numbers is $division");
  print("The remainder of dividing the two numbers is $modulo");
}
