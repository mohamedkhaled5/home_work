void main() {
  //var data type
  /**
   * Accept any value but do not carry hair after it is determined
   */

  //dynamic data type
  /**
   * Accepts any value and can be changed at any time.
   */

  dynamic age = 19;
  print(age);
  age = 'mohamed';
  print(age);

  var greeting = 'Hi';
  greeting = 'welcome';

  print(greeting);

  num pi = 3.14159;

  print(pi.toInt());
  print(pi.toStringAsFixed(3));
}
